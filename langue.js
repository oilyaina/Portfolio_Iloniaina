// ===== MENU BURGER =====
const burgerBtn = document.querySelector("#burger");
const burgerMenu = document.querySelector("#burger-menu");
const burgerOverlay = document.querySelector("#burger-overlay");

function ouvrirMenu() {
  burgerMenu.classList.add("open");
  burgerOverlay.classList.add("open");
  burgerBtn.textContent = "✕";
}

function fermerMenu() {
  burgerMenu.classList.remove("open");
  burgerOverlay.classList.remove("open");
  burgerBtn.textContent = "☰";
}

burgerBtn.addEventListener("click", () => {
  burgerMenu.classList.contains("open") ? fermerMenu() : ouvrirMenu();
});

burgerOverlay.addEventListener("click", fermerMenu);

// Ferme le menu au clic sur un lien
burgerMenu.querySelectorAll("a").forEach(lien => {
  lien.addEventListener("click", fermerMenu);
});

// ===== MODAL MENTIONS LÉGALES =====
const modal = document.querySelector("#modal-mentions");

function ouvrirModal() {
  modal.classList.add("open");
}

function fermerModal() {
  modal.classList.remove("open");
}

document.querySelector("#open-mentions").addEventListener("click", () => {
  fermerMenu();
  ouvrirModal();
});

document.querySelector("#open-mentions-footer").addEventListener("click", ouvrirModal);
document.querySelector("#close-mentions").addEventListener("click", fermerModal);

// Ferme la modal en cliquant en dehors
modal.addEventListener("click", (e) => {
  if (e.target === modal) fermerModal();
});