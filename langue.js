function traduireListe(id, items) {
  const ul = document.querySelector("#" + id + " ul");
  if (!ul) return;
  ul.innerHTML = items.map(item => `<li>${item}</li>`).join("");
}

function traduire(langue) {
  const l = langues[langue];

  // Éléments simples via data-i18n
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const cle = el.getAttribute("data-i18n");
    if (l[cle]) el.textContent = l[cle];
  });

  // Listes
  traduireListe("block-exp", l.expItems);
  traduireListe("block-formation", l.formationItems);
  traduireListe("block-hard-skills", l.hardSkillsItems);
  traduireListe("block-soft-skills", l.softSkillsItems);
  traduireListe("block-langues", l.languesItems);
}

document.querySelector("#btn-fr").addEventListener("click", () => traduire("fr"));
document.querySelector("#btn-en").addEventListener("click", () => traduire("en"));

